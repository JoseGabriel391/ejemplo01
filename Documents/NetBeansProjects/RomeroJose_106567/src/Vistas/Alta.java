    public void cargarComboPaciente(){
        Paciente pac = null;
        Controlador controlador = new Controlador();
        ArrayList<Paciente> lista = controlador.obtenerPaciente();
        DefaultComboBoxModel model = new DefaultComboBoxModel();
        
        for (Paciente paciente : lista) {
            model.addElement(paciente);
            
            pac = paciente;
        }
        cmbPaciente.setModel(model);
        cmbPaciente.setSelectedItem(pac);
    }
    
    public void cargarComboEmpleado(){
        Empleado empl = null;
        Controlador controlador = new Controlador();
        ArrayList<Empleado> lista = controlador.obtenerRecepcionista();
        DefaultComboBoxModel model = new DefaultComboBoxModel();
        
        for (Empleado empleado : lista) {
            model.addElement(empleado);
            
            empl = empleado;
        }
        cmbEmpleado.setModel(model);
        cmbEmpleado.setSelectedItem(empl);
    }
    

