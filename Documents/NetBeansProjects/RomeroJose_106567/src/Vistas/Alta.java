    private void btnCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarActionPerformed
        String nombreVisita = txtNombreVisita.getText();
        Paciente paciente = (Paciente)cmbPaciente.getSelectedItem();
        Empleado empleado = (Empleado)cmbEmpleado.getSelectedItem();
        int duracion = Integer.parseInt(txtDuracion.getText());
        
        Visita v = new Visita(paciente, empleado, nombreVisita, duracion);
        Controlador controlador = new Controlador();
        
        boolean inserto = controlador.insertarVisita(v);
        if(inserto)
            JOptionPane.showMessageDialog(this, "Alta ok");
        else
            JOptionPane.showMessageDialog(this, "Fallo el alta");
    }//GEN-LAST:event_btnCargarActionPerformed

